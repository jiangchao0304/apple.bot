package com.suzy.entity;

public class Stores {

	private String storeNumber;
	
	private String storeName;
	
	private String storeEnabled;
	
	private String sellEdition;
	
	private String storeCity;
	
	public String getStoreNumber() {
		return storeNumber;
	}
	public void setStoreNumber(String storeNumber) {
		this.storeNumber = storeNumber;
	}
	public String getStoreName() {
		return storeName;
	}
	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}
	public String getStoreEnabled() {
		return storeEnabled;
	}
	public void setStoreEnabled(String storeEnabled) {
		this.storeEnabled = storeEnabled;
	}
	public String getSellEdition() {
		return sellEdition;
	}
	public void setSellEdition(String sellEdition) {
		this.sellEdition = sellEdition;
	}
	public String getStoreCity() {
		return storeCity;
	}
	public void setStoreCity(String storeCity) {
		this.storeCity = storeCity;
	}
	

	
}
